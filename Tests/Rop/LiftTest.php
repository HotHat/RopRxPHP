namespace Rop\Test;
use PHPUnit\Framework\TestCase;
use function Rop\lift;
use Rop\Result;


class LiftTest extends TestCase
{
    private $increment;
    public function setUp()
    {
        $this->increment = function ($int) {
            return $int + 1;
        };
    }
    public function test_return_right()
    {
        $this->assertEquals(
            Result::success(2),
            lift($this->increment)(1)
        );
    }
}